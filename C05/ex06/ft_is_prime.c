int	ft_is_prime(int nb)
{
	int	a;

	if (nb <= 1)
		return (0);
	a = 1 + nb / 4;
	while (nb % a != 0)
	{
		if (a == 1)
		{
			return (1);
		}
		a--;
	}
	if (a == 1)
		return (1);
	return (0);
}
