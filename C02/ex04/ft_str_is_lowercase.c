int	ft_str_is_lowercase(char *str)
{
	int	tmp;

	tmp = 0;
	while (str[tmp] != '\0')
	{
		if ((str[tmp] < 97) || (str[tmp] > 122))
			return (0);
		tmp++;
	}
	return (1);
}
