int	ft_str_is_numeric(char *str)
{
	int	tmp;

	tmp = 0;
	while (str[tmp] != '\0')
	{
		if ((str[tmp] < 48) || (str[tmp] > 57))
			return (0);
		tmp++;
	}
	return (1);
}
